//Code to demonstrate toString() method of Array Class to print Arrays with ease
import java.util.*;
public class ArrayClass 
{
    public static void main(String[] args)
    {
        int ar[]={10,20,15,22,35};
        System.out.println("Integer Array: "+Arrays.toString(ar));
        String arr[]={"Aabir","Anushka","Simba","Gauri"};
        System.out.println("Integer Array: "+Arrays.toString(arr));
    }
}
