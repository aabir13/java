//Code to demonstate the varying data types
public class DataType 
{
    public static void main(String[] args)
    {
        //Character data type
        char a = 'A';
        //Integer data type
        int b = 121;
        //Byte and short data type for smaller values
        byte c = 1;
        short d = 11;
        //Float data type for smaller decimal places
        float e = 3.14f;
        //Double data type for larger decimal places
        double f =  3.141592653589;
        
        //Printing the values
        System.out.println("char: "+a);
        System.out.println("int: "+b);
        System.out.println("byte: "+c);
        System.out.println("short: "+d);
        System.out.println("float: "+e);
        System.out.println("double: "+f);
    }
    
}
